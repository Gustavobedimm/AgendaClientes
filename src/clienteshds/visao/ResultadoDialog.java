    private void jLabel31MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel31MouseClicked
        if (txtCodigo.getText().equals("") || txtCodigo.getText() == null) {
            JOptionPane.showMessageDialog(null, "O campo código é obrigatório.");
            txtCodigo.requestFocus();
        } else {
            if ((new Integer(txtCodigo.getText())) < 1) {
                JOptionPane.showMessageDialog(null, "O campo não pode ser menor que 1.");
            } else {
                Empresa empresa = new Empresa();
                EmpresaController empresaController = new EmpresaController();
                empresa.setId_cliente(new Integer(txtCodigo.getText()));
                empresa.setCnpj(txtCnpj.getText());
                empresa.setRazaoSocial(txtRazaoSocial.getText());
                empresa.setFantasia(txtNomeFantasia.getText());
                empresa.setTelefone1(txtTelefone1.getText());
                empresa.setTelefone2(txtTelefone2.getText());
                empresa.setIpServidor(txtIpServidor.getText());
                empresa.setSenha(txtSenha.getText());
                empresa.setDominio(txtDominio.getText());
                empresa.setRegimeContabil(txtRegimeContabil.getSelectedItem().toString());
                empresa.setContabilidade(txtContabilidade.getText());
                empresa.setResponsavel(txtResponsavel.getText());
                empresa.setTelefoneContador(txtTelefone3.getText());
                empresa.setVencimentoSistema(txtVencimentoSistema.getText());
                empresa.setVencimentoCertificado(txtVencimentoCertificado.getText());
                empresa.setObs(txtObs.getText());
                empresa.setPasta(txtPasta.getText());
                if (CheckBoxNfe.isSelected()) {
                    empresa.setNfe("S");
                } else {
                    empresa.setNfe("N");
                }
                if (CheckBoxNfce.isSelected()) {
                    empresa.setNfce("S");
                } else {
                    empresa.setNfce("N");
                }
                if (CheckBoxNfse.isSelected()) {
                    empresa.setNfse("S");
                } else {
                    empresa.setNfse("N");
                }

                if (empresaController.valida(empresa) == false) {
                    JOptionPane.showMessageDialog(null, "O campo CNPJ é obrigatório.");
                    txtCnpj.requestFocus();
                } else {
                    try {
                        if (empresaDAO.consultaCnpj(txtCnpj.getText()) == null) {
                            try {
                                empresaController.adiciona(empresa);
                                dispose();
                            } catch (ClassNotFoundException ex) {
                                Logger.getLogger(ResultadoDialog.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        } else if (btnApagar.isEnabled()) {
                            empresaDAO.AlterarEmpresa(empresa);
                            dispose();
                        } else {
                            JOptionPane.showMessageDialog(null, "CNPJ já cadastrado.");
                            dispose();
                        }
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(ResultadoDialog.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
            }
        }
    }//GEN-LAST:event_jLabel31MouseClicked

    private void btnApagarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnApagarMouseClicked
        if (btnApagar.isEnabled()) {
            int Button = JOptionPane.YES_NO_OPTION;
            int dialogButton = JOptionPane.showConfirmDialog(rootPane, "Deseja mesmo EXCLUIR este cliente ?", "Excluir Cliente", Button);
            if (dialogButton == JOptionPane.YES_OPTION) {
                try {
                    empresaDAO.ExcluirEmpresa(txtCnpj.getText());
                    limpaTela();
                    dispose();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ResultadoDialog.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnApagarMouseClicked

