    public void carregaTela(String cnpj) {
        Empresa empresa = new Empresa();
        try {
            empresa = empresaDAO.consultaCnpj(cnpj);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ResultadoDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtCodigo.setText(empresa.getId_cliente().toString());
        txtCnpj.setText(empresa.getCnpj());
        txtRazaoSocial.setText(empresa.getRazaoSocial());
        txtNomeFantasia.setText(empresa.getFantasia());
        txtTelefone1.setText(empresa.getTelefone1());
        txtTelefone2.setText(empresa.getTelefone2());
        txtIpServidor.setText(empresa.getIpServidor());
        txtSenha.setText(empresa.getSenha());
        txtDominio.setText(empresa.getDominio());
        txtRegimeContabil.setSelectedItem(empresa.getRegimeContabil());
        txtContabilidade.setText(empresa.getContabilidade());
        txtResponsavel.setText(empresa.getResponsavel());
        txtTelefone3.setText(empresa.getTelefoneContador());
        txtVencimentoSistema.setText(empresa.getVencimentoSistema());
        txtVencimentoCertificado.setText(empresa.getVencimentoCertificado());
        txtObs.setText(empresa.getObs());
        txtPasta.setText(empresa.getPasta());
        if (empresa.getNfe().equals("S")) {
            CheckBoxNfe.setSelected(true);
        }
        if (empresa.getNfce().equals("S")) {
            CheckBoxNfce.setSelected(true);
        }
        if (empresa.getNfse().equals("S")) {
            CheckBoxNfse.setSelected(true);
        }
    }

